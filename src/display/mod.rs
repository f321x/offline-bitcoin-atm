pub trait AtmDisplay<SPI, DELAY>
where
    SPI: embedded_hal::spi::SpiDevice,
    DELAY: embedded_hal::delay::DelayNs,
{
    fn home_screen(
        &mut self,
        spi: &mut SPI,
        delay: &mut DELAY,
    ) -> Result<(), Box<dyn std::error::Error>>;
    fn show_inserted_amount(
        &mut self,
        spi: &mut SPI,
        delay: &mut DELAY,
        amount_string: &str,
    ) -> Result<(), Box<dyn std::error::Error>>;
    fn show_qr(
        &mut self,
        spi: &mut SPI,
        delay: &mut DELAY,
        qr_content: &str,
    ) -> Result<(), Box<dyn std::error::Error>>;
    fn clean(&mut self, spi: &mut SPI, delay: &mut DELAY)
        -> Result<(), Box<dyn std::error::Error>>;
    fn show_orangeclock(
        &mut self,
        spi: &mut SPI,
        delay: &mut DELAY,
        data: &MempoolData,
        items: &DisplayItems,
        currency: &PriceCurrency,
    ) -> Result<(), Box<dyn std::error::Error>>;
}
