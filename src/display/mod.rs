pub fn parse_rotation(s: &str) -> DisplayRotation {
    match s {
        "0" => DisplayRotation::Rotate0,
        "90" => DisplayRotation::Rotate90,
        "180" => DisplayRotation::Rotate180,
        _ => DisplayRotation::Rotate270,
    }
}

