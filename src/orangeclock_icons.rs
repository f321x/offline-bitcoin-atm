/// Icon bitmap metadata for rendering with `embedded_graphics`.
#[derive(Clone, Copy)]
pub struct IconBitmap {
    pub width: u32,
    pub height: u32,
    pub data: &'static [u8],
}

/// Identifies which data item a row represents, for icon selection.
#[derive(Clone, Copy)]
pub enum OrangeClockItem {
    BlockHeight,
    PriceUsd,
    PriceEur,
    MoscowTime,
    Fees,
    HalvingCountdown,
    DifficultyAdjustment,
    MempoolSize,
}

#[derive(Clone, Copy)]
pub enum IconSize {
    Small, // 25px — for 1.54" and 2.13"
    Large, // 35px — for 2.7"
}

impl IconSize {
    pub fn height(self) -> i32 {
        match self {
            IconSize::Small => 25,
            IconSize::Large => 35,
        }
    }
}

/// Look up the icon bitmap for a given data item and size.
/// Returns `None` for items that have no dedicated icon.
pub fn get_icon(item: OrangeClockItem, size: IconSize) -> Option<IconBitmap> {
    match (item, size) {
        (OrangeClockItem::BlockHeight, IconSize::Small) => Some(IconBitmap {
            width: 28,
            height: 25,
            data: &ICON_BLOCK_25,
        }),
        (OrangeClockItem::BlockHeight, IconSize::Large) => Some(IconBitmap {
            width: 40,
            height: 35,
            data: &ICON_BLOCK_35,
        }),
        (OrangeClockItem::PriceUsd, IconSize::Small) => Some(IconBitmap {
            width: 29,
            height: 25,
            data: &ICON_USD_25,
        }),
        (OrangeClockItem::PriceUsd, IconSize::Large) => Some(IconBitmap {
            width: 40,
            height: 35,
            data: &ICON_USD_35,
        }),
        (OrangeClockItem::PriceEur, IconSize::Small) => Some(IconBitmap {
            width: 28,
            height: 25,
            data: &ICON_EUR_25,
        }),
        (OrangeClockItem::PriceEur, IconSize::Large) => Some(IconBitmap {
            width: 40,
            height: 35,
            data: &ICON_EUR_35,
        }),
        (OrangeClockItem::MoscowTime, IconSize::Small) => Some(IconBitmap {
            width: 28,
            height: 25,
            data: &ICON_SATS_25,
        }),
        (OrangeClockItem::MoscowTime, IconSize::Large) => Some(IconBitmap {
            width: 40,
            height: 35,
            data: &ICON_SATS_35,
        }),
        (OrangeClockItem::Fees, IconSize::Small) => Some(IconBitmap {
            width: 27,
            height: 25,
            data: &ICON_FEES_25,
        }),
        (OrangeClockItem::Fees, IconSize::Large) => Some(IconBitmap {
            width: 40,
            height: 35,
            data: &ICON_FEES_35,
        }),
        (OrangeClockItem::HalvingCountdown, IconSize::Small) => Some(IconBitmap {
            width: 28,
            height: 25,
            data: &ICON_HALVING_25,
        }),
        (OrangeClockItem::HalvingCountdown, IconSize::Large) => Some(IconBitmap {
            width: 40,
            height: 35,
            data: &ICON_HALVING_35,
        }),
        (OrangeClockItem::DifficultyAdjustment, _) => None,
        (OrangeClockItem::MempoolSize, _) => None,
    }
}

